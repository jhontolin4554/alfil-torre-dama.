
public class reglas {
    public int fil;
    public int  colum;

    public reglas(int fil,int colum){
        this.fil=fil;
        this.colum=colum;
    }
    public int getFil() {
        return fil;
    }

    public int getColum() {
        return colum;
    }

    public void setFil(int fil) {
        this.fil = fil;
    }

    public void setColum(int colum) {
        this.colum = colum;
    }
    
}